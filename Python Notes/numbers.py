#Numbers in Python 
''' Numbers in Python are of three types
1. int - integer value
2. float - decimal value
3.complex - stands for complex numbers'''

#int
# integer is a whole number, positive or negative number and it is of type int.
x = 5 
print(x)

#float 
#float is a whole number, positive or negative number with decimals

f = 8.8
print(f)

#complex 
#complex is imaginary number

c = 3j + 4
print(c)